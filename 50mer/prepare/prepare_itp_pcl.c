void print_help()
{
	printf ("usage:re [options]\n \
	options:\n \
        -n number of monomers\n \
	-h help\n");
	exit(1);
}

int parse_options (int argc, char *argv[])
{
	if (argc==1) print_help();
	while (--argc && (*++argv)[0] == '-') 
	{
		char c;
		c = *++argv[0];
		switch (c) 
		{
			case 'n':
				nomPLA=atoi(*++argv);
				break;
			case 'h' :
				print_help();
				break;
			default:
				printf ("\ncorre: incorrect input, help:-h \
					\n\n", argv[0]);
				exit (1);
		
		}
		--argc;
	}
	return 0;
}
